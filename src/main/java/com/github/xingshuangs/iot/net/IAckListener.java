package com.github.xingshuangs.iot.net;


/**
 * Ack listener interface, do success or failed.
 * (响应监听器)
 *
 * @author xingshuang
 */
public interface IAckListener<T> {

    /**
     * Execute success.
     * (成功执行业务)
     *
     * @param ack ack data
     */
    void ok(T ack);

    /**
     * Execute failed.
     * (失败执行业务)
     *
     * @param message error message
     */
    void fail(String message);
}
